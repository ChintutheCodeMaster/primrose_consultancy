// Compact summary of each scholarship for AI prompt (token-efficient)
export function buildScholarshipPromptList(): string {
  return scholarships.map(s => [
    `ID: ${s.id}`,
    `Name: ${s.name}`,
    `Coverage: ${s.coverage === 'full' ? 'Full funding' : s.amount}`,
    `Degrees: ${s.degreeTypes.join(', ')}`,
    `Study in: ${s.studyCountries.length ? s.studyCountries.join(', ') : 'Flexible'}`,
    `Citizens: ${s.citizenships.length ? s.citizenships.join(', ') : 'Any'}`,
    `Fields: ${s.fieldsOfStudy.length ? s.fieldsOfStudy.join(', ') : 'Any'}`,
    `Tags: ${s.tags.join(', ')}`,
    `Eligibility: ${s.eligibilitySummary}`,
  ].join(' | ')).join('\n');
}
